# define the Vehicle class
class Vehicle:
    
  def __init__(self, name, color, price):
    self.name = name
    self.color = color
    self.price = price
        
  def description(self):
    return 'Car name: {} Color: {} Price: {}'.format(self.name, self.color, self.price)

