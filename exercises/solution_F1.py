# Initialize counter = -4
counter = -4

#1 Code the while loop
while counter != 0:
  #2 Print Great job
  print("Great job")
  #3 if counter positive
  if counter > 0:
    # Increase counter
    counter = counter - 1
  #4 else (negative)
  else:
    # Decrease counter
    counter = counter + 1
  #5 Print counter
  print(counter)
